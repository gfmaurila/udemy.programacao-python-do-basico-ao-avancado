
# Exemplos de Operadores de Comparação

# Igualdade (==)
a = 10
b = 10
print("a == b:", a == b)  # True

# Diferença (!=)
a = 10
b = 20
print("a != b:", a != b)  # True

# Maior que (>)
a = 15
b = 10
print("a > b:", a > b)  # True

# Menor que (<)
a = 5
b = 8
print("a < b:", a < b)  # True

# Maior ou igual (>=)
a = 7
b = 7
print("a >= b:", a >= b)  # True

# Menor ou igual (<=)
a = 3
b = 10
print("a <= b:", a <= b)  # True

# Comparação entre strings
nome1 = "ana"
nome2 = "ana"
nome3 = "Ana"
print("nome1 == nome2:", nome1 == nome2)  # True
print("nome1 == nome3:", nome1 == nome3)  # False (case sensitive)

# Comparação entre listas
lista1 = [1, 2, 3]
lista2 = [1, 2, 3]
lista3 = [3, 2, 1]
print("lista1 == lista2:", lista1 == lista2)  # True
print("lista1 == lista3:", lista1 == lista3)  # False
